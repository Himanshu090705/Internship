// Library v/s Framework
// Library -> A library is a collection of pre-written code that can be used for specific tasks.    eg - axios
// Framework -> A framework is a set of pre-written code that provides a structure for developing software apps.    eg - express

// Express -> A Node.js web application framework that helps us to make web apps
//          -> it is used for server side programming

// Express usage ->
// 1) listen for incoming request
// 2) parse the request
// 3) match responses with routes
// 4) send suitable response

// Port -> ports are logical endpoints of a network connection that is a used to exchange information between a webserver and webclient
